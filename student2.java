package JAVA;

public class student2 {
    //if-else if ladder
    public static void main(String[]args){
        String city="Delhi";
        if(city=="Meerut"){
            System.out.println("City is Meerut");
        }else if(city=="Noida"){
            System.out.println("City is Noida");
        }else if(city=="Agra"){
            System.out.println("City is Agra");
        }else{
            System.out.println(city);
        }
    }
}
