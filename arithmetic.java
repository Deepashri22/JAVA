package JAVA;

public class arithmetic {
    public static void main(String[]args){
        int a=12, b=5;

        //addition operator
        System.out.println("a+b is:"+(a+b));

        //subtraction operator
        System.out.println("a-b is:"+(a-b));

        //multiplication operator
        System.out.println("a*b is:"+(a*b));

        //division operator
        System.out.println("a/b is:"+(a/b));

        //modula operator
        System.out.println("a%b is:"+(a%b));
    } 
}
